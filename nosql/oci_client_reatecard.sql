    id STRING AS UUID GENERATED BY DEFAULT,
    tenant_id STRING,
    subscription_id STRING,
    currency_iso_code STRING,
    currency_name STRING,
    currency_std_precision NUMBER,
    discretionary_discount_percentage STRING,
    net_unit_price DOUBLE,
    overage_price DOUBLE,
    product_billing_category STRING,
    product_name STRING,
    product_part_number STRING,
    product_product_category STRING,
    product_ucm_rate_card_part_type STRING,
    product_unit_of_measure STRING,
    time_end TIMESTAMP(0),
    time_start TIMESTAMP(0),
    up_to_quantity LONG,
    is_active BOOLEAN,
    last_executed_time TIMESTAMP(0),
